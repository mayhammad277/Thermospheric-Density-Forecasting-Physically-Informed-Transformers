def run_pipeline(data_dir: str, seq_len: int = 25, epochs: int = 100):
    """
    End-to-end pipeline: load → preprocess → merge → features → train → evaluate.

    data_dir must contain files whose names start with:
      'champ', 'goes', 'omni', 'initial_states'
    Each CSV must have a 'Timestamp' column (or 'time'/'date').
    """
    print("Loading data...")
    # CHAMP files have no consistent prefix — load by excluding the other known types.
    # This mirrors the original notebook logic:
    #   champ_files = [f for f in all_files if f.endswith('.csv')
    #                  and f not in initial_state_files + omni2_files + goes_files]
    champ_df    = load_csv_files(data_dir,
                                  exclude_prefixes=['initial_states', 'omni2', 'omni-', 'omni_', 'goes'])
    goes_df_raw = load_csv_files(data_dir, prefix='goes')
    omni_df_raw = load_csv_files(data_dir, prefix='omni')
    init_df     = load_csv_files(data_dir, prefix='initial_states')

    for name, df in [('champ (exclusion)', champ_df), ('goes', goes_df_raw),
                     ('omni', omni_df_raw), ('initial_states', init_df)]:
        if df.empty:
            raise RuntimeError(
                f"No files loaded for '{name}' in {data_dir}.\n"
                "  For CHAMP: adjust exclude_prefixes to cover all non-CHAMP file prefixes.\n"
                "  For others: check filenames start with goes / omni / initial_states."
            )
        print(f"  {name}: {df.shape}  columns: {list(df.columns[:5])} ...")

    print("\nPreprocessing...")
    # preprocess_* return DataFrames with DatetimeIndex named 'Timestamp'
    omni_clean = preprocess_omni(omni_df_raw)
    goes_clean = preprocess_goes(goes_df_raw)

    print("Engineering features...")
    omni_clean = engineer_solar_wind_features(omni_clean)
    omni_clean = engineer_kp_features(omni_clean)
    omni_clean = add_solar_cycle_feature(omni_clean)
    omni_clean = add_dst_forecast_features(omni_clean)
    omni_clean = add_kp_forecast_features(omni_clean)
    omni_clean = create_lags(omni_clean, 'Kp_index', lags=[1, 2, 3, 4, 5, 7, 19, 38])
    goes_clean = engineer_goes_features(goes_clean)

    print("Merging sources...")
    # Pass init_df as-is — _ensure_timestamp_column inside align_and_merge handles it
    merged = align_and_merge(champ_df, omni_clean, goes_clean, init_df)

    print("Cleaning NaNs...")
    merged = drop_flag_columns(merged)
    merged = smart_fill_nans(merged)

    target = 'Orbit Mean Density (kg/m^3)'
    if target not in merged.columns:
        raise KeyError(
            f"Target column '{target}' not found after merge. "
            f"Available columns: {list(merged.columns)}"
        )
    merged = merged.dropna(subset=[target])
    print(f"Final dataset: {merged.shape}")

    # ── Chronological train / val / test split (never shuffle time series!)
    n         = len(merged)
    train_end = int(n * 0.70)
    val_end   = int(n * 0.85)
    train_df  = merged.iloc[:train_end]
    val_df    = merged.iloc[train_end:val_end]
    test_df   = merged.iloc[val_end:]

    X_train, y_train, scaler = make_sequences(train_df, target, seq_len, fit_scaler=True)
    X_val,   y_val,   _      = make_sequences(val_df,   target, seq_len,
                                               scaler=scaler, fit_scaler=False)
    X_test,  y_test,  _      = make_sequences(test_df,  target, seq_len,
                                               scaler=scaler, fit_scaler=False)

    print(f"  X_train: {X_train.shape}  X_val: {X_val.shape}  X_test: {X_test.shape}")

    input_dim = X_train.shape[2]
    model = TemporalDensityTransformer(
        input_dim=input_dim, seq_len=seq_len,
        num_heads=4, num_layers=3, hidden_dim=128, dropout=0.1
    )

    print("\nTraining...")
    model, history = train_model(model, X_train, y_train, X_val, y_val,
                                  epochs=epochs, log_target=True)

    # Plot training history
    plt.figure(figsize=(10, 4))
    plt.plot(history['train'], label='Train Loss')
    plt.plot(history['val'],   label='Val Loss')
    plt.xlabel('Epoch'); plt.ylabel('MSE (log-space)'); plt.legend()
    plt.title('Training History — Thermospheric Density Model')
    plt.tight_layout()
    plt.savefig('training_history.png', dpi=150)
    plt.show()

    return model, scaler, merged


