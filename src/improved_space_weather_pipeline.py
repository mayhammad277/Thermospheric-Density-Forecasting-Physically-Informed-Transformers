def smart_fill_nans(df: pd.DataFrame, target_col: str = 'Orbit Mean Density (kg/m^3)') -> pd.DataFrame:
    """
    FIX: Replaces deprecated `fillna(method='ffill')` syntax.
    Uses time-aware interpolation for target, forward/backward fill for features.
    """
    df = df.copy()

    # Target: interpolate before fill (preserves physical trend better)
    if target_col in df.columns:
        df[target_col] = df[target_col].interpolate(method='linear')
        df[target_col] = df[target_col].ffill().bfill()

    # Features: forward fill then backward fill
    df = df.ffill().bfill()

    # Drop columns with > 45% NaN
    nan_frac = df.isna().mean()
    df = df.loc[:, nan_frac <= 0.45]

    return df


