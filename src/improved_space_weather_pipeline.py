def make_sequences(df: pd.DataFrame, target_col: str, seq_len: int,
                   scaler: MinMaxScaler = None, fit_scaler: bool = True):
    """
    Creates (X, y) sequences for time-series models.

    FIX: Original prep_data() had the array-building inside the loop,
    causing X and y to be reset on each iteration.

    FIX: Returns the fitted scaler so the same transform can be applied
    to validation/test data (prevents data leakage).
    """
    feature_cols = [c for c in df.select_dtypes(include='number').columns if c != target_col]
    X_df = df[feature_cols]
    y_series = df[target_col]

    if scaler is None:
        scaler = MinMaxScaler()
    if fit_scaler:
        X_scaled = scaler.fit_transform(X_df)
    else:
        X_scaled = scaler.transform(X_df)

    X_arr, y_arr = [], []
    for i in range(len(X_scaled) - seq_len):
        X_arr.append(X_scaled[i: i + seq_len])
        y_arr.append(y_series.iloc[i + seq_len])

    return np.array(X_arr), np.array(y_arr), scaler


