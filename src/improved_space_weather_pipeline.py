def _read_csvs(directory: str, filenames: list) -> pd.DataFrame:
    """
    Internal helper: reads a list of filenames from directory into one DataFrame.
    Auto-detects the timestamp column and parses it.
    """
    dfs = []
    for f in filenames:
        df = pd.read_csv(os.path.join(directory, f))
        ts_col = next((c for c in df.columns if c.lower() in ('timestamp', 'time', 'date')), None)
        if ts_col:
            df[ts_col] = pd.to_datetime(df[ts_col], errors='coerce')
            if ts_col != 'Timestamp':
                df = df.rename(columns={ts_col: 'Timestamp'})
        dfs.append(df)

    if not dfs:
        return pd.DataFrame()
    return pd.concat(dfs).sort_values('Timestamp').reset_index(drop=True)


def load_csv_files(directory: str, prefix: str = None, n_files: int = None,
                   exclude_prefixes: list = None, filenames: list = None) -> pd.DataFrame:
    """
    Flexible CSV loader. Three usage modes:

    1. PREFIX mode (default) — files whose names START with `prefix`:
          load_csv_files(dir, prefix='goes')

    2. EXCLUSION mode — all CSVs except those starting with excluded prefixes.
       Matches how CHAMP files are identified in the original notebook:
          load_csv_files(dir, exclude_prefixes=['initial_states', 'omni2', 'goes'])

    3. EXPLICIT mode — pass an exact list of filenames:
          load_csv_files(dir, filenames=['champ_001.csv', 'champ_002.csv'])

    All modes sort by numeric order in the filename and honour `n_files`.
    """
    all_csvs = sorted(
        [f for f in os.listdir(directory) if f.endswith('.csv')],
        key=lambda x: tuple(map(int, re.findall(r'\d+', x))) if re.findall(r'\d+', x) else (x,)
    )

    if filenames is not None:
        selected = [f for f in filenames if f in all_csvs]

    elif exclude_prefixes is not None:
        # Exclusion mode: everything that does NOT start with any excluded prefix
        selected = [
            f for f in all_csvs
            if not any(f.startswith(p) for p in exclude_prefixes)
        ]

    elif prefix is not None:
        selected = [f for f in all_csvs if f.startswith(prefix)]

    else:
        raise ValueError("Provide at least one of: prefix, exclude_prefixes, or filenames.")

    if n_files:
        selected = selected[:n_files]

    return _read_csvs(directory, selected)


