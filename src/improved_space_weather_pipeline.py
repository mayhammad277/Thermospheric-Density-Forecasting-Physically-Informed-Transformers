# ─── 8. SPACE WEATHER FORECASTING FEATURES ───────────────────────────────────

def add_dst_forecast_features(df: pd.DataFrame, dst_col: str = 'Dst_index_nT') -> pd.DataFrame:
    """
    Features specifically useful for Dst (geomagnetic storm) forecasting.
    Based on Burton et al. 1975 and O'Brien & McPherron 2000 empirical models.
    """
    df = df.copy()
    if dst_col not in df.columns:
        return df

    # Rate of change of Dst (injection vs. recovery)
    df['Dst_rate'] = df[dst_col].diff()

    # Dst thresholds for storm classification
    df['storm_minor']    = (df[dst_col] < -30).astype(int)
    df['storm_moderate'] = (df[dst_col] < -50).astype(int)
    df['storm_intense']  = (df[dst_col] < -100).astype(int)
    df['storm_extreme']  = (df[dst_col] < -250).astype(int)

    # Lags critical for Dst recovery time (~6–24 hours)
    for lag_h in [1, 3, 6, 12, 24]:
        periods = lag_h  # if hourly resolution
        df[f'Dst_lag_{lag_h}h'] = df[dst_col].shift(periods)

    return df


def add_kp_forecast_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Features for Kp index forecasting (3h resolution target).
    Based on Boberg & Ridley 2005 and Newell et al. 2008 coupling functions.
    """
    df = df.copy()

    # Newell coupling function: dΦ/dt ∝ v^(4/3) * Bt^(2/3) * sin^(8/3)(θ/2)
    # where Bt = transverse B, θ = clock angle
    if all(c in df.columns for c in ['SW_Plasma_Speed_km_s', 'BY_nT_GSE', 'BZ_nT_GSM']):
        v = df['SW_Plasma_Speed_km_s'].clip(lower=1)
        by = df['BY_nT_GSE']
        bz = df['BZ_nT_GSM']
        Bt = np.sqrt(by**2 + bz**2).clip(lower=1e-6)
        clock = np.arctan2(by, bz)
        df['newell_coupling'] = (v ** (4/3)) * (Bt ** (2/3)) * (np.sin(clock / 2) ** (8/3))

    return df


