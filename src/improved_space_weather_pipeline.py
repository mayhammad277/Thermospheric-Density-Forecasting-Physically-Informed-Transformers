def preprocess_omni(df: pd.DataFrame) -> pd.DataFrame:
    """
    Cleans OMNI solar wind data:
      - Replaces fill values (-999.9, -1, 9999.99, etc.) with NaN
      - Keeps physically meaningful columns
      - Returns a DataFrame with DatetimeIndex named 'Timestamp'
    """
    fill_values = [-999.9, -999, -9999, -1, 9999.99, 99999.9, 9999999.]
    df = df.copy()

    # Detect the timestamp column (handles 'Timestamp', 'timestamp', 'time', etc.)
    ts_col = next((c for c in df.columns if c.lower() in ('timestamp', 'time', 'date')), None)
    if ts_col is None:
        raise KeyError("No timestamp column found in OMNI data. Columns: " + str(list(df.columns)))

    df[ts_col] = pd.to_datetime(df[ts_col], errors='coerce')
    df = df.set_index(ts_col)
    df.index.name = 'Timestamp'  # normalise index name

    # Replace known fill values
    df = df.replace(fill_values, np.nan)

    keep_cols = [
        'Kp_index', 'R_Sunspot_No', 'Dst_index_nT', 'ap_index_nT', 'f10.7_index',
        'AE_index_nT', 'AL_index_nT', 'AU_index_nT', 'Lyman_alpha',
        'Proton_flux_>1_Mev', 'Proton_flux_>2_Mev', 'Proton_flux_>4_Mev',
        'Proton_flux_>10_Mev', 'Proton_flux_>30_Mev', 'Proton_flux_>60_Mev',
        'SW_Plasma_Speed_km_s', 'SW_Proton_Density_N_cm3', 'SW_Plasma_Temperature_K',
        'BZ_nT_GSM', 'BX_nT_GSE_GSM', 'BY_nT_GSE', 'BZ_nT_GSE',
        'Flow_pressure', 'Plasma_Beta', 'Alfen_mach_number', 'Magnetosonic_Mach_number',
    ]
    keep_cols = [c for c in keep_cols if c in df.columns]
    # Return with DatetimeIndex — callers use .reset_index() when they need a column
    return df[keep_cols]


def preprocess_goes(df: pd.DataFrame, sigma: float = 1.0) -> pd.DataFrame:
    """
    Resamples GOES X-ray flux to 5-minute bins, applies Gaussian smoothing,
    and adds log1p-transformed flux columns (better than raw flux for modeling).
    Returns a DataFrame with DatetimeIndex named 'Timestamp'.
    """
    df = df.copy()

    ts_col = next((c for c in df.columns if c.lower() in ('timestamp', 'time', 'date')), None)
    if ts_col is None:
        raise KeyError("No timestamp column found in GOES data. Columns: " + str(list(df.columns)))

    df[ts_col] = pd.to_datetime(df[ts_col])
    df = df.resample('5min', on=ts_col).mean()
    df.index.name = 'Timestamp'  # normalise index name
    df = df.dropna(axis=1, how='all')

    for col in ['xrsa_flux', 'xrsb_flux']:
        if col in df.columns:
            df[col] = df[col].fillna(df[col].mean())
            df[f'{col}_smoothed'] = gaussian_filter(df[col].values, sigma=sigma)
            # FIX: log1p handles near-zero and tiny positive values safely
            df[f'{col}_log1p'] = np.log1p(df[col].clip(lower=0))

    return df


# ─── 3. FEATURE ENGINEERING ──────────────────────────────────────────────────

def engineer_solar_wind_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Physically motivated solar wind features for thermospheric density prediction.
    """
    df = df.copy()

    # FIX: Guard log transform against zero/negative values
    if 'SW_Plasma_Speed_km_s' in df.columns:
        safe_v = df['SW_Plasma_Speed_km_s'].clip(lower=1.0)
        df['SW_Plasma_Speed_km_s_log'] = np.log(safe_v)

    # Signed cubic Bz — captures southward reconnection nonlinearity
    if 'BZ_nT_GSM' in df.columns:
        df['BZ_nT_GSM_cubic'] = np.sign(df['BZ_nT_GSM']) * np.abs(df['BZ_nT_GSM']) ** 3
        # Southward Bz only (key reconnection driver)
        df['BZ_south'] = df['BZ_nT_GSM'].clip(upper=0).abs()

    # Solar wind ram pressure = 0.5 * rho * v^2 (dynamic pressure proxy)
    if 'SW_Plasma_Speed_km_s' in df.columns and 'SW_Proton_Density_N_cm3' in df.columns:
        df['SW_ram_pressure'] = (
            0.5 * df['SW_Proton_Density_N_cm3'] * df['SW_Plasma_Speed_km_s'] ** 2
        )

    # Electric field Ey = -Vx * Bz (reconnection driver)
    if 'SW_Plasma_Speed_km_s' in df.columns and 'BZ_nT_GSM' in df.columns:
        df['merging_electric_field'] = -df['SW_Plasma_Speed_km_s'] * df['BZ_nT_GSM']

    return df


def engineer_kp_features(df: pd.DataFrame, kp_col: str = 'Kp_index') -> pd.DataFrame:
    """
    Engineers Kp-derived features. Includes storm phase tracking.
    """
    df = df.copy()
    if kp_col not in df.columns:
        print(f"Warning: '{kp_col}' not found.")
        return df

    # Temporal derivatives
    df[f'{kp_col}_diff_1'] = df[kp_col].diff(1)
    df[f'{kp_col}_diff_2'] = df[kp_col].diff(2)

    # Rolling stats at multiple windows (3h, 9h, 18h, 24h, 72h)
    for window in [1, 3, 6, 8, 24]:
        h = window * 3
        df[f'{kp_col}_rolling_mean_{h}h'] = df[kp_col].rolling(window, min_periods=1).mean()
        df[f'{kp_col}_rolling_max_{h}h']  = df[kp_col].rolling(window, min_periods=1).max()
        df[f'{kp_col}_rolling_std_{h}h']  = df[kp_col].rolling(window, min_periods=1).std()

    # Storm flags
    for thr in [4, 5, 7]:
        col = f'{kp_col}_above_{thr}'
        df[col] = (df[kp_col] > thr).astype(int)
        df[f'{col}_persistence'] = df[col].rolling(8, min_periods=1).sum()

    # Storm phase: are we in main phase (Kp rising) or recovery?
    df[f'{kp_col}_rising'] = (df[f'{kp_col}_diff_1'] > 0).astype(int)

    return df


def engineer_goes_features(df: pd.DataFrame, rolling_window: int = 72) -> pd.DataFrame:
    """
    Rolling integral (energy proxy) + flare peak detection for XRS flux.
    window=72 assumes 5-minute samples → 6 hours.
    """
    df = df.copy()
    for col in ['xrsa_flux', 'xrsb_flux']:
        if col in df.columns:
            df[f'{col}_integral'] = df[col].rolling(rolling_window, min_periods=1).sum()
            # Peak flag: local max within 1-hour window
            roll_max = df[col].rolling(12, min_periods=1).max()
            df[f'{col}_is_peak'] = (df[col] == roll_max).astype(int)

    return df


def create_lags(df: pd.DataFrame, col: str, lags: list, unit_hours: float = 3.0) -> pd.DataFrame:
    """
    Unified lag feature creator. `lags` are in multiples of `unit_hours`.
    FIX: Merged duplicate create_specific_kp_lags / create_specific_lags functions.
    """
    df = df.copy()
    for lag in lags:
        name = f'{col}_lag_{int(lag * unit_hours)}h'
        df[name] = df[col].shift(lag)
    return df


def add_solar_cycle_feature(df: pd.DataFrame, f107_col: str = 'f10.7_index') -> pd.DataFrame:
    """
    Adds 81-day smoothed F10.7 (proxy for solar EUV flux controlling
    background thermospheric density). This is one of the most important
    drivers for Orbit Mean Density.
    """
    df = df.copy()
    if f107_col in df.columns:
        # 81-day = 81 * 24 hours; for hourly OMNI data that's 81*24 rows
        # Adjust window for your actual resolution
        df['F10.7_81d_smooth'] = df[f107_col].rolling(window=81*24, min_periods=1).mean()
        df['F10.7_deviation'] = df[f107_col] - df['F10.7_81d_smooth']
    return df


# ─── 4. MERGING + ALIGNMENT ──────────────────────────────────────────────────

def _ensure_timestamp_column(df: pd.DataFrame) -> pd.DataFrame:
    """
    Helper: guarantees the DataFrame has a plain 'Timestamp' column (not index).
    Works whether Timestamp is already a column, the index, or named differently.
    """
    df = df.copy()
    # If the index IS the timestamp, reset it to a column
    if isinstance(df.index, pd.DatetimeIndex) or df.index.name == 'Timestamp':
        df = df.reset_index()
        # reset_index uses the index name; normalise to 'Timestamp'
        if df.index.name and df.index.name != 'Timestamp':
            df = df.rename(columns={df.columns[0]: 'Timestamp'})
        elif 'index' in df.columns:
            df = df.rename(columns={'index': 'Timestamp'})
    # Make sure the column is datetime
    if 'Timestamp' in df.columns:
        df['Timestamp'] = pd.to_datetime(df['Timestamp'])
    return df.sort_values('Timestamp').reset_index(drop=True)


def align_and_merge(champ_df, omni_df, goes_df, initial_state_df,
                    omni_shift_min=45, goes_shift_min=30) -> pd.DataFrame:
    """
    Merges multi-source data with physically motivated propagation delays:
      - OMNI: shifted +45 min (solar wind travel time L1 → magnetopause)
      - GOES: shifted +30 min (typical flare → ionosphere response)

    Accepts DataFrames with either a DatetimeIndex or a 'Timestamp' column.
    Uses merge_asof (nearest-neighbour merge) — correct for asynchronous data.
    """
    # ── OMNI: ensure DatetimeIndex, upsample 1h→5min, shift propagation delay
    if not isinstance(omni_df.index, pd.DatetimeIndex):
        omni_df = omni_df.set_index('Timestamp')
    omni_5min = omni_df.resample('5min').interpolate(method='cubic')
    omni_shifted = omni_5min.copy()
    omni_shifted.index = omni_shifted.index + pd.Timedelta(minutes=omni_shift_min)
    omni_shifted = omni_shifted.reset_index().rename(columns={'Timestamp': 'Timestamp'})
    # After reset_index the column will be named after the index ('Timestamp')
    # but guard against 'index' rename too
    if 'index' in omni_shifted.columns and 'Timestamp' not in omni_shifted.columns:
        omni_shifted = omni_shifted.rename(columns={'index': 'Timestamp'})
    omni_shifted['Timestamp'] = pd.to_datetime(omni_shifted['Timestamp'])

    # ── GOES: ensure DatetimeIndex, shift flare response delay
    if not isinstance(goes_df.index, pd.DatetimeIndex):
        goes_df = goes_df.set_index('Timestamp')
    goes_shifted = goes_df.copy()
    goes_shifted.index = goes_shifted.index + pd.Timedelta(minutes=goes_shift_min)
    goes_shifted = goes_shifted.reset_index()
    if 'index' in goes_shifted.columns and 'Timestamp' not in goes_shifted.columns:
        goes_shifted = goes_shifted.rename(columns={'index': 'Timestamp'})
    goes_shifted['Timestamp'] = pd.to_datetime(goes_shifted['Timestamp'])

    # ── CHAMP: make sure it has a plain Timestamp column
    champ_df = _ensure_timestamp_column(champ_df)

    # ── merge_asof requires sorted data
    omni_shifted = omni_shifted.sort_values('Timestamp')
    goes_shifted = goes_shifted.sort_values('Timestamp')

    merged = pd.merge_asof(champ_df, omni_shifted, on='Timestamp', direction='nearest')
    merged = pd.merge_asof(merged,   goes_shifted, on='Timestamp', direction='nearest')

    if initial_state_df is not None and not initial_state_df.empty:
        init_flat = _ensure_timestamp_column(initial_state_df)
        merged = pd.merge_asof(merged, init_flat, on='Timestamp', direction='nearest')

    return merged


