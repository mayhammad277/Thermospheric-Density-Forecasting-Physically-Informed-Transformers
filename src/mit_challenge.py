def rolling_window_evaluation(full_df, model, target_series, window_size, forecast_length, scaler=None, device='cpu', num_predictions=5):
    """
    Performs rolling window evaluation by iteratively removing the last
    'forecast_length' elements, predicting them, and comparing with actual values.

    Args:
        full_df (pd.DataFrame): The complete preprocessed DataFrame with 'Timestamp' as index.
        model (torch.nn.Module): The trained PyTorch model.
        target_series (pd.Series): The complete target variable Series with 'Timestamp' as index.
        window_size (int): The size of the historical input window.
        forecast_length (int): The number of future steps the model predicts.
        scaler (MinMaxScaler, optional): The scaler used for the features during training.
        device (str): The device to use for the model ('cpu' or 'cuda').
        num_predictions (int): The number of rolling predictions to perform from the end of the data.

    Returns:
        pd.DataFrame: A DataFrame containing the actual and predicted values for all
                      performed predictions, along with overall evaluation metrics.
    """
    model.eval()
    model.to(device)

    print(f"Length of full_df received: {len(full_df)}")
    print(f"Length of target_series received: {len(target_series)}")

    full_df = full_df.sort_index()
    target_series = target_series.sort_index()

    results = []
    all_actual = []
    all_predicted = []
    all_timestamps = []

    for i in range(num_predictions):
        # Calculate the starting index for the evaluation window
        eval_end_index = len(full_df) - (i * forecast_length)
        eval_start_index = eval_end_index - (window_size + forecast_length)

        if eval_start_index < 0:
            print(f"Warning: Not enough data for {num_predictions} rolling predictions with current window and forecast length.")
            break

        evaluation_df = full_df.iloc[eval_start_index:eval_end_index]
        evaluation_target = target_series.iloc[eval_start_index:eval_end_index]

        # Prepare the input window
        last_window = evaluation_df.iloc[:-forecast_length].values

        if scaler:
            scaled_window = scaler.transform(last_window)
        else:
            scaled_window = last_window

        input_tensor = torch.tensor(scaled_window, dtype=torch.float32).unsqueeze(0).to(device)

        # Make the prediction
        with torch.no_grad():
            future_predictions = model(input_tensor).cpu().numpy()

        # Get the actual target values
        actual_values = evaluation_target.iloc[-forecast_length:].values.reshape(1, -1)
        timestamps = evaluation_df.index[-forecast_length:]

        results.append(pd.DataFrame({
            'Timestamp': timestamps,
            'Actual': actual_values.flatten(),
            'Predicted': future_predictions.flatten()
        }).set_index('Timestamp'))

        all_actual.extend(actual_values.flatten())
        all_predicted.extend(future_predictions.flatten())
        all_timestamps.extend(timestamps)

    if not results:
        return None

    final_results_df = pd.concat(results)

    overall_mse = mean_squared_error(all_actual, all_predicted)
    overall_mae = mean_absolute_error(all_actual, all_predicted)

    print("\nRolling Window Evaluation Results:")
    print(final_results_df)
    print(f"\nOverall Mean Squared Error (MSE) across {len(all_actual)} predictions: {overall_mse:.4f}")
    print(f"Overall Mean Absolute Error (MAE) across {len(all_actual)} predictions: {overall_mae:.4f}")

    return final_results_df
