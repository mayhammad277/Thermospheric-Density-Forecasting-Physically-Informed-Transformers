def find_and_read_matching_files_multi_source(date_dict, data_directories):
    """
    Takes a dictionary containing start and end timestamps, then searches
    multiple directories for CSV files (prefixed with 'champ-', 'goes-', or 'omni-')
    whose timestamps fall within this range.

    For 'champ' files, it uses the specific 'champ_-\d{5}-\d{8}_to_\d{8}\.csv' pattern.
    For 'goes' and 'omni' files, it uses the pattern to extract date ranges.

    Args:
        date_dict (dict): A dictionary with 'start_timestamp' and 'end_timestamp'
                           keys. The values should be Pandas Timestamp objects
                           or strings that can be converted to datetime objects.
                           Example: {'start_timestamp': pd.Timestamp('2000-08-02 04:50:33'),
                                     'end_timestamp': pd.Timestamp('2000-12-04 18:00:35')}
        data_directories (dict): A dictionary where keys are source names
                                   ('champ', 'goes', 'omni') and values are the
                                   corresponding directory paths.
                                   Example: {'champ': '/path/to/champ_data',
                                             'goes': '/path/to/goes_data',
                                             'omni': '/path/to/omni_data'}

    Returns:
        dict: A dictionary where keys are the file paths of the opened CSV files
              and values are the Pandas DataFrames read from those files.
              The keys will be prefixed with the source name to avoid collisions.
              Returns an empty dictionary if no matching files are found or
              if there's an error with the provided dates or directories.
    """
    try:
        start_date = pd.to_datetime(date_dict['start_timestamp'])
        end_date = pd.to_datetime(date_dict['end_timestamp'])
    except KeyError:
        print("Error: The input dictionary must contain 'start_timestamp' and 'end_timestamp' keys.")
        return {}
    except ValueError:
        print("Error: Could not parse 'start_timestamp' or 'end_timestamp' into datetime objects.")
        return {}

    matching_files = {}
    valid_sources = ['champ', 'goes', 'omni']

    for source, data_directory in data_directories.items():
        if source not in valid_sources:
            print(f"Warning: Invalid source name '{source}'. Skipping directory.")
            continue
        if not os.path.isdir(data_directory):
            print(f"Warning: Directory not found: {data_directory}")
            continue

        for filename in os.listdir(data_directory):
            if filename.endswith(".csv") and filename.startswith(f"{source}-"):
                try:
                    file_start_date = None
                    file_end_date = None
                    match = re.search(r"-(\d{8})_to_(\d{8})\.csv", filename)

                    if match:
                        file_start_str, file_end_str = match.groups()
                        file_start_date = datetime.strptime(file_start_str, "%Y%m%d").date()
                        file_end_date = datetime.strptime(file_end_str, "%Y%m%d").date()

                        print('champ')
                        if re.match(r"champ_-\d{5}-\d{8}_to_\d{8}\.csv", filename):
                                if (file_start_date <= end_date.date() and file_end_date >= start_date.date()):
                                    filepath = os.path.join(data_directory, filename)
                                    try:
                                        matching_files[f"{source}-{filepath}"] = pd.read_csv(filepath)
                                        print(f"Successfully read file: {filename} from {source}")
                                    except Exception as e:
                                        print(f"Error reading data file '{filename}' from {source}: {e}")
                        if source in ['goes', 'omni']:
                            if (file_start_date <= end_date.date() and file_end_date >= start_date.date()):
                                filepath = os.path.join(data_directory, filename)
                                try:
                                    matching_files[f"{source}-{filepath}"] = pd.read_csv(filepath)
                                    print(f"Successfully read file: {filename} from {source}")
                                except Exception as e:
                                    print(f"Error reading data file '{filename}' from {source}: {e}")
                    else:
                        print(f"Warning: Could not parse date range from filename '{filename}' in {source}. Skipping.")
                except ValueError:
                    print(f"Warning: Invalid date format in filename '{filename}' from {source}. Skipping.")

    if not matching_files:
        print("No matching data files found within the specified date range in the given directories.")

    return matching_files

