import os
import re
import json
from datetime import datetime

def organize_files_by_date_and_type(root_dir, output_json="file_organization.json"):
    """
    Pulls files by date (excluding the first date range), classifies them by type,
    and stores their full paths and metadata in a JSON file.

    Args:
        root_dir (str): The root directory to search for files.
        output_json (str): The name of the JSON file to create.
    """
    organization = {
        "champ": {},
        "goes": {},
        "omni": {},
        "initial_states": {}
    }
    file_counts = {
        "champ": 0,
        "goes": 0,
        "omni": 0,
        "initial_states": 0
    }

    # Regex to extract date range from the filename
    date_pattern = re.compile(r"-(\d{8})_to_(\d{8})\.csv") or re.compile(r"champ_-\d{5}-\d{8}_to_\d{8}\.csv") or re.compile(r"grace2-\d{5}-\d{8}_to_\d{8}\.csv")

    for folder in os.listdir(root_dir):
      for filename in os.listdir(os.path.join(root_dir, folder)):
        print(filename)
        match = date_pattern.search(filename)
        if match:
            start_date_str = match.group(1)
            end_date_str = match.group(2)

            try:
                start_date = datetime.strptime(start_date_str, '%Y%m%d').date()
                end_date = datetime.strptime(end_date_str, '%Y%m%d').date()
                first_excluded_date = datetime(2000, 8, 6).date()

                # Exclude files with the first date range (2000-08-06 to 2000-08-06)
                if not (start_date == first_excluded_date and end_date == first_excluded_date):
                    full_path = os.path.join(root_dir+'/'+folder, filename)

                    if filename.startswith("champ"):
                        file_type = "champ"
                    elif filename.startswith("goes"):
                        file_type = "goes"
                    elif filename.startswith("grace2"):
                        file_type = "champ"
                    elif filename.startswith("gr-of1"):
                        file_type = "champ"
                    elif filename.startswith("omni"):
                        file_type = "omni"

                    elif filename.startswith("initial_states"):
                        file_type = "initial_states"
                    else:
                        continue  # Skip files that don't match the prefixes

                    file_counts[file_type] += 1
                    organization[file_type][file_counts[file_type]] = {
                        "date_from": start_date.isoformat(),
                        "date_to": end_date.isoformat(),
                        "full_path": full_path
                    }
            except ValueError:
                print(f"Warning: Could not parse date from filename: {filename}")

    with open(output_json, 'w') as f:
        json.dump(organization, f, indent=4)

    print(f"File organization information saved to: {output_json}")

