def drop_mostly_nan_columns(df, threshold=0.45):
    """
    Drops columns from a pandas DataFrame if the percentage of NaN values
    exceeds the specified threshold.

    Args:
        df (pd.DataFrame): The input DataFrame.
        threshold (float, optional): The threshold (between 0 and 1) for the
                                     maximum allowed percentage of NaN values.
                                     Defaults to 0.85.

    Returns:
        pd.DataFrame: A new DataFrame with columns exceeding the NaN
                      threshold dropped.
    """


    total_rows = len(df)
    columns_to_drop = []
    for col in df.columns:
        nan_count = df[col].isnull().sum()
        nan_percentage = nan_count / total_rows
        if nan_percentage > threshold:
            columns_to_drop.append(col)

    df_cleaned = df.drop(columns=columns_to_drop)
    df_cleaned.drop(["xrsb_flag_x","xrsa_flag_x"],axis=1,inplace=True)
    return df_cleaned

