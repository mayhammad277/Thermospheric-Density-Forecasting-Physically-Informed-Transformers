def load_matching_files_from_organization(organization_json_path, date_range):
    """
    Loads CSV files listed in the organization.json whose date ranges overlap
    with the provided date_range.

    Args:
        organization_json_path (str): Path to the organization.json file.
        date_range (dict): A dictionary with 'start_timestamp' and 'end_timestamp'
                           as Pandas Timestamp objects.

    Returns:
        dict: A dictionary where keys are the full file paths of the loaded
              CSV files, and values are the Pandas DataFrames.
    """
    try:
        with open(organization_json_path, 'r') as f:
            organization = json.load(f)
        print(f"Successfully loaded organization.json: {organization}")
    except FileNotFoundError:
        print(f"Error: Organization JSON file not found at '{organization_json_path}'.")
        return {}
    except json.JSONDecodeError:
        print(f"Error: Could not decode JSON from '{organization_json_path}'.")
        return {}

    start_date_range = date_range['start_timestamp'].date()
    end_date_range = date_range['end_timestamp'].date()
    loaded_files = {}

    for source, files in organization.items():
        if source == 'initial_states':
            continue  # Skip initial states for now

        for file_info in files.values():
            try:
                date_from_str = file_info['date_from']
                date_to_str = file_info['date_to']
                full_path = file_info['full_path']

                file_start_date = datetime.strptime(date_from_str, "%Y-%m-%d").date()
                file_end_date = datetime.strptime(date_to_str, "%Y-%m-%d").date()

                # Check if the file's date range overlaps with the given date range
                if (file_start_date <= end_date_range and file_end_date >= start_date_range):
                    print(f"Attempting to load: {full_path}")
                    try:
                        df = pd.read_csv(full_path)
                        loaded_files[full_path] = df
                        print(f"Successfully loaded: {full_path}")
                    except FileNotFoundError:
                        print(f"Warning: File not found at '{full_path}'. Skipping.")
                    except Exception as e:
                        print(f"Error reading file '{full_path}': {e}")

            except KeyError as e:
                print(f"Warning: Missing key '{e}' in organization data for source '{source}'. Skipping entry.")
            except ValueError:
                print(f"Warning: Invalid date format in organization data for source '{source}'. Skipping entry.")

    if not loaded_files:
        print("No matching files found within the specified date range in the organization data.")

    return loaded_files

