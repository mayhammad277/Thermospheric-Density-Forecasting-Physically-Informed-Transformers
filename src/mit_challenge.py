def engineer_kp_features(df, kp_col='Kp_index'):
    """
    Engineers a variety of features from the Kp index.

    Args:
        df (pd.DataFrame): DataFrame with a datetime index containing the Kp index.
        kp_col (str): Name of the Kp index column.

    Returns:
        pd.DataFrame: DataFrame with engineered Kp features added.
    """
    df_engineered = df.copy()

    if kp_col not in df_engineered.columns:
        print(f"Warning: Kp column '{kp_col}' not found.")
        return df_engineered

    # 1. Temporal Derivatives and Changes
    df_engineered[f'{kp_col}_diff_1'] = df_engineered[kp_col].diff(1)
    df_engineered[f'{kp_col}_diff_2'] = df_engineered[kp_col].diff(2)
    df_engineered[f'{kp_col}_rate_of_change_3h'] = df_engineered[kp_col].diff(1) # Assuming 3-hourly Kp

    # 2. Statistical Features over Windows (adjust window sizes as needed)
    for window in [3, 6, 8, 12, 24]: # Window sizes in number of 3-hour intervals (hours: 9, 18, 24, 36, 72)
        df_engineered[f'{kp_col}_rolling_mean_{window*3}h'] = df_engineered[kp_col].rolling(window=window, min_periods=1).mean()
        df_engineered[f'{kp_col}_rolling_std_{window*3}h'] = df_engineered[kp_col].rolling(window=window, min_periods=1).std()
        df_engineered[f'{kp_col}_rolling_min_{window*3}h'] = df_engineered[kp_col].rolling(window=window, min_periods=1).min()
        df_engineered[f'{kp_col}_rolling_max_{window*3}h'] = df_engineered[kp_col].rolling(window=window, min_periods=1).max()
        df_engineered[f'{kp_col}_rolling_q90_{window*3}h'] = df_engineered[kp_col].rolling(window=window, min_periods=1).quantile(0.9)

    # 3. Binary or Categorical Features (example thresholds)
    storm_thresholds = [4, 5, 7]
    for threshold in storm_thresholds:
        df_engineered[f'{kp_col}_above_{threshold}'] = (df_engineered[kp_col] > threshold).astype(int)
        df_engineered[f'{kp_col}_above_{threshold}_persistence'] = (df_engineered[f'{kp_col}_above_{threshold}']).rolling(window=8, min_periods=1).sum() # Check persistence over ~24 hours

    # (More advanced: Time since last storm - requires more complex logic to track events)

    return df_engineered





def create_engineered_features(df):
    """
    Adds engineered features to the input DataFrame.

    Args:
        df (pd.DataFrame): The input DataFrame containing the necessary columns.
                           Assumes columns 'SW_Plasma_Speed_km_s', 'BZ_nT_GSM',
                           'xrsa_flux', and 'xrsb_flux' exist.

    Returns:
        pd.DataFrame: The DataFrame with the following new features added:
                      - 'SW_Plasma_Speed_km_s_log': Log-transformed SW_Plasma_Speed_km_s
                      - 'BZ_nT_GSM_cubic': Signed cubic transform of BZ_nT_GSM
                      - 'xrsa_flux_integral': 6-hour integral of xrsa_flux (assuming 5-minute intervals)
                      - 'xrsb_flux_integral': 6-hour integral of xrsb_flux (assuming 5-minute intervals)
    """
    df_processed = df.copy()

    # Log-transform SW_Plasma_Speed_km_s
    if 'SW_Plasma_Speed_km_s' in df_processed.columns:
        df_processed['SW_Plasma_Speed_km_s_log'] = np.log(df_processed['SW_Plasma_Speed_km_s'].replace(0, np.nan)).fillna(method='ffill') # Replace 0 with NaN before log
    else:
        print("Warning: 'SW_Plasma_Speed_km_s' column not found.")

    # Signed cubic transform for BZ_nT_GSM
    if 'BZ_nT_GSM' in df_processed.columns:
        df_processed['BZ_nT_GSM_cubic'] = np.sign(df_processed['BZ_nT_GSM']) * np.abs(df_processed['BZ_nT_GSM']) ** 3
    else:
        print("Warning: 'BZ_nT_GSM' column not found.")

    # Integrate XRS_flux over preceding 6 hours (assuming 5-minute intervals)
    rolling_window = 72
    if 'xrsa_flux' in df_processed.columns:
        df_processed['xrsa_flux_integral'] = df_processed['xrsa_flux'].rolling(window=rolling_window, min_periods=1).sum().fillna(0)
    else:
        print("Warning: 'xrsa_flux' column not found.")

    if 'xrsb_flux' in df_processed.columns:
        df_processed['xrsb_flux_integral'] = df_processed['xrsb_flux'].rolling(window=rolling_window, min_periods=1).sum().fillna(0)
    else:
        print("Warning: 'xrsb_flux' column not found.")

    # It's generally better to fill NaNs *after* all feature engineering
    df_processed = df_processed.fillna(df_processed.mean(numeric_only=True))

    return df_processed

