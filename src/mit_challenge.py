def remove_trend_polynomial(df, column, order=1):
    """
    Removes the trend from a specified column of a DataFrame using polynomial fitting.

    Args:
        df (pd.DataFrame): The input DataFrame with a datetime index.
        column (str): The name of the column to process.
        order (int): The order of the polynomial to fit (e.g., 1 for linear, 2 for quadratic).

    Returns:
        pd.DataFrame: A new DataFrame with the trend removed from the specified column.
    """
    df_detrended = df.copy()
    time = np.arange(len(df_detrended.index))
    coeffs = np.polyfit(time, df_detrended[column], order)
    trend = np.polyval(coeffs, time)
    df_detrended[column + '_detrended'] = df_detrended[column] - trend
    return df_detrended

