def keep_highest_correlation(df, target_column, percentage=0.5, threshold=None, absolute=True):
    """
    Keeps the top percentage of columns with the highest correlation (or above a
    threshold) to the specified target variable in a pandas DataFrame.

    Args:
        df (pd.DataFrame): The input DataFrame.
        target_column (str): The name of the target variable column.
        percentage (float, optional): The percentage (between 0 and 1) of top
                                     correlated columns to keep. For example,
                                     0.5 keeps the top 50% of features.
                                     Defaults to 0.5.
        threshold (float, optional): A minimum absolute correlation value to keep
                                     columns. If provided, this takes precedence
                                     over the percentage. Only columns with an
                                     absolute correlation greater than or equal to
                                     this threshold are kept. Defaults to None.
        absolute (bool, optional): Whether to consider the absolute value of the
                                   correlation. If True (default), the magnitude
                                   of the correlation is used. If False, the
                                   sign of the correlation is considered.

    Returns:
        pd.DataFrame: A new DataFrame containing only the target column and the
                      top correlated columns (based on the percentage or
                      threshold).
    """
    if target_column not in df.columns:
        raise ValueError(f"Target column '{target_column}' not found in DataFrame.")

    correlations = df.corr()[target_column].drop(target_column)

    if absolute:
        correlations = correlations.abs()

    if threshold is not None:
        relevant_correlations = correlations[correlations >= threshold]
        top_correlated_columns = relevant_correlations.sort_values(ascending=False).index.tolist()
    else:
        num_features = len(correlations)
        n_to_keep = max(1, int(percentage * num_features))  # Ensure at least one feature is kept
        top_correlated_columns = correlations.nlargest(n_to_keep).index.tolist()

    columns_to_keep = [target_column] + top_correlated_columns
    # Ensure only existing columns are kept
    columns_to_keep = [col for col in columns_to_keep if col in df.columns]

    return df[columns_to_keep].copy()

def prep_data_all(df_list):
    """
    Combines a list of pandas DataFrames based on their time indices,
    aligns timestamps, handles missing values, and normalizes numerical columns.

    Args:
        df_list (list): A list of pandas DataFrames with datetime indices.

    Returns:
        tuple: A tuple containing:
            - combined_df (pandas.DataFrame): The merged and preprocessed DataFrame.
            - to_predict (pandas.Series): The target variable ('Orbit Mean Density (kg/m^3)').
    """
    if not df_list:
        return pd.DataFrame(), pd.Series()

    # Convert index to datetime and drop NaT
    processed_dfs = []
    for df in df_list:
        df.index = pd.to_datetime(df.index, errors='coerce')
        df = df[df.index.notna()]
        processed_dfs.append(df.resample('5min').mean()) # Resample each DataFrame

    # Merge all DataFrames based on the union of their indices
    if not processed_dfs:
        return pd.DataFrame(), pd.Series()

    combined_index = processed_dfs[0].index
    for df in processed_dfs[1:]:
        combined_index = combined_index.union(df.index)

    merged_df = pd.DataFrame(index=combined_index)
    for df in processed_dfs:
        merged_df = merged_df.merge(df, left_index=True, right_index=True, how='left')

    combined_df = merged_df.sort_index().reset_index().rename(columns={'index': 'Timestamp'})

    try:
        combined_df["Orbit Mean Density (kg/m^3)"] = combined_df['Orbit Mean Density (kg/m^3)'] \
            .interpolate(method='linear').fillna(method='bfill').fillna(method='ffill')
    except KeyError:
        pass  # Handle case where target column might be missing

    combined_df = combined_df.fillna(method='ffill').interpolate(method='linear').fillna(method='bfill')
    print("combined_df head",combined_df.head())
    # Handle Numeric Columns
    numeric_df = combined_df.select_dtypes(include=['number']).copy()
    to_predict = numeric_df["Orbit Mean Density (kg/m^3)_x"].copy()
    #numeric_df = numeric_df.drop(["Orbit Mean Density (kg/m^3)_x","Orbit Mean Density (kg/m^3)_y"], errors='ignore')
    numeric_df = numeric_df.drop(["Orbit Mean Density (kg/m^3)_x"], errors='ignore')
    # Normalize numerical columns
    numerical_columns = numeric_df.columns
    if not numerical_columns.empty:
        scaler = MinMaxScaler()
        numeric_df[numerical_columns] = scaler.fit_transform(numeric_df[numerical_columns])

    # Re-insert the target variable if it existed

    return numeric_df



