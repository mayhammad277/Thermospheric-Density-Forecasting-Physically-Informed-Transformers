def remove_outliers_zscore(df, column, threshold=3):
    """
    Removes outliers from a specified column of a DataFrame using the Z-score method.

    Args:
        df (pd.DataFrame): The input DataFrame.
        column (str): The name of the column to process.
        threshold (float): The number of standard deviations from the mean to consider as an outlier.

    Returns:
        pd.DataFrame: A new DataFrame with outliers removed from the specified column.
    """
    df_cleaned = df.copy()
    z_scores = np.abs((df_cleaned[column] - df_cleaned[column].mean()) / df_cleaned[column].std())
    df_cleaned = df_cleaned[z_scores <= threshold]
    return df_cleaned

