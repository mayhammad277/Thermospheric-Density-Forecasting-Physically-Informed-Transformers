# Function to download NASA OMNI dataset
def download_omni_data(start_year, end_year):
    url = f"https://omniweb.gsfc.nasa.gov/cgi/nx1.cgi?activity=retrieve&res=hour&spacecraft=omni2&start_date={start_year}-01-01&end_date={end_year}-12-31&vars=10,14"
    response = requests.get(url)
    with open("omni_data.txt", "w") as f:
        f.write(response.text)
    print("OMNI data downloaded.")

