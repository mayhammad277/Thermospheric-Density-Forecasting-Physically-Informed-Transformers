# Function to visualize OMNI data
def plot_omni_data(data):
    plt.figure(figsize=(12, 6))

    # Plot F10.7 solar flux
    plt.subplot(2, 1, 1)
    plt.plot(data.index, data["f10.7_index"], label="F10.7 Solar Flux", color="blue")
    plt.title("F10.7 Solar Flux")
    plt.xlabel("Time")
    plt.ylabel("Flux")
    plt.legend()

    # Plot Ap geomagnetic index
    plt.subplot(2, 1, 2)
    plt.plot(data.index, data["AL_index_nT"], label="AL_index_nT", color="red")
    plt.title("Ap Geomagnetic Index")
    plt.xlabel("Time")
    plt.ylabel("Index")
    plt.legend()

    plt.tight_layout()
    plt.show()

