def fix_time(df1,df2):
  df1['Timestamp'] = pd.to_datetime(df1['Timestamp'])

  df1.set_index('Timestamp', inplace=True)

  df2['Timestamp'] = pd.to_datetime(df2['Timestamp'])

  df2.set_index('Timestamp', inplace=True)

  combined_index = df1.index.union(df2.index)  # Union of both indices
  df1 = df1.reindex(combined_index)
  df2 = df2.reindex(combined_index)

  # Interpolate missing values
  df1.interpolate(method='time', inplace=True)
  df2.interpolate(method='time', inplace=True)

  # Combine the two DataFrames
  merged_df = pd.concat([df1, df2], axis=0)

  # Interpolate missing values in the merged DataFrame
  merged_df.interpolate(method='time', inplace=True)

  return merged_df

