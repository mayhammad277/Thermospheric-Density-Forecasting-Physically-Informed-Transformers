class TemporalSpatialTransformer(nn.Module):
    def __init__(self, input_dim, sequence_length, num_heads, num_layers, hidden_dim):
        super().__init__()
        self.embedding = nn.Linear(input_dim, hidden_dim)  # Project input_dim to hidden_dim
        self.transformer = nn.Transformer(
            d_model=hidden_dim,
            nhead=num_heads,
            num_encoder_layers=num_layers,
            num_decoder_layers=num_layers,
            dim_feedforward=hidden_dim * 4,
            batch_first=True,
        )
        self.fc = nn.Linear(hidden_dim, 1)  # Final layer to predict a single value

    def forward(self, x):
        # x shape: (batch_size, sequence_length, input_dim)
        x = self.embedding(x)  # Shape: (batch_size, sequence_length, hidden_dim)
        x = self.transformer(x, x)  # Shape: (batch_size, sequence_length, hidden_dim)
        x = x.mean(dim=1)  # Pool across the sequence dimension
        return self.fc(x)  # Shape: (batch_size, 1)


