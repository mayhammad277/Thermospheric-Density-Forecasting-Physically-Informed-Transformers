# Define the EKF application function
def apply_ekf(df, state_transition_function, observation_function, jacobian_state_transition, jacobian_observation, process_noise=0.01, observation_noise=0.1):
    """
    Applies the Extended Kalman Filter (EKF) to a DataFrame with a 'Value' column.

    Parameters:
        df (pd.DataFrame): Input DataFrame with a 'Value' column and a 'Timestamp' index.
        state_transition_function (callable): Non-linear state transition function.
        observation_function (callable): Non-linear observation function.
        jacobian_state_transition (callable): Jacobian of the state transition function.
        jacobian_observation (callable): Jacobian of the observation function.
        process_noise (float): Process noise covariance.
        observation_noise (float): Observation noise covariance.

    Returns:
        pd.DataFrame: DataFrame with an additional 'Smoothed_Value' column containing the EKF estimates.
    """
    # Initialize EKF
    ekf = ExtendedKalmanFilter(dim_x=1, dim_z=1)
    ekf.x = np.array([df['Value'].mean()])  # Initial state estimate
    ekf.P = np.array([1])  # Initial error covariance
    ekf.Q = np.array([process_noise])  # Process noise covariance
    ekf.R = np.array([observation_noise])  # Observation noise covariance
    ekf.F = jacobian_state_transition  # State transition Jacobian
    ekf.H = jacobian_observation  # Observation Jacobian

    # Apply EKF
    smoothed_values = []
    for z in df['Value'].values:
        if np.isnan(z):
            # Predict step only if observation is missing
            ekf.predict()
        else:
            # Predict and update steps
            ekf.predict()
            ekf.update(np.array([z]), HJacobian=jacobian_observation, Hx=observation_function)
        smoothed_values.append(ekf.x[0])

    # Add smoothed values to DataFrame
    df['Smoothed_Value'] = smoothed_values
    return df

