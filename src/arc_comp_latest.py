def absolute_date_to_datetime(absolute_date):
    """Convert Orekit AbsoluteDate to Python datetime"""
    components = absolute_date.getComponents(TimeScalesFactory.getUTC())
    date = components.getDate()
    time_ = components.getTime()
    print(time_)
    print(type(time_))
    # Extract whole seconds and microseconds from fractional part
    seconds = int(time_.getSecond())  # Whole seconds
    microseconds = int((time_.getSecond() % 1) * 1_000_000)  # Convert fraction to microseconds

    return datetime(
        date.getYear(),
        date.getMonth(),
        date.getDay(),
        time_.getHour(),
        time_.getMinute(),
        seconds,
        microseconds  # Use microseconds instead of milliseconds
    )

