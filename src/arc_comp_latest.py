def calculate_lat_lon(position, frame):
    """Calculate latitude and longitude from position vector"""
    earth = CelestialBodyFactory.getEarth()
    earth_transform = frame.getTransformTo(earth.getBodyFrame(), position.getDate())
    earth_position = earth_transform.transformPosition(position)

    geodetic_point = earth.getBodyShape().transform(earth_position, frame, position.getDate())
    return geodetic_point.getLatitude() * 180 / np.pi, geodetic_point.getLongitude() * 180 / np.pi

